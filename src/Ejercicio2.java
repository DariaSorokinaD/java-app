
public class Ejercicio2 {
    
  //  2. Ejemplo de uso de do-while. Programa Java que muestre los números del 1 al 100 utilizando la instrucción do..while.
    public static void main(String args[]){
        
        int numero=0;
        
        do { 
            numero++;
            System.out.println(numero);
            
        }while(numero<100);
            
        
       
        
        
    }
}
