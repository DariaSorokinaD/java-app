
package ejercicioWhile1;


public class EjercicioWhile1 {
    //1. Ejemplo de uso de while: Programa Java que muestre los números del 1 al 100 utilizando la instrucción while.
    
    public static void main(String args[]){
        int numero=0;
        
        while (numero < 100){
            numero++;
        //si se pone fuera del while solo muestra el resultado final sin el proceso.    
         System.out.println(numero);   
        }
        
    }
    
}
